    private void ConfirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmBtnActionPerformed
        // TODO add your handling code here:
        String userPwd = getCurrentPassword();
        String newPwd = getNewPassword();
        String newPwdConfirm = getConfirmedPassword();
        boolean validChange = true;

        if (!userPwd.equals(dummyPwd)) {
            invalidateField(CurrentPasswordField, CurrentPasswordIncorrect);
            validChange = false;
        }
        if (!newPwdConfirm.equals(newPwd) || newPwdConfirm.length() == 0) {
            invalidateField(ConfirmNewPasswordField, ConfirmNewPasswordIncorrect);
            validChange = false;
        }
        if (newPwd.length() == 0) {
            invalidateField(NewPasswordField, NewPasswordIncorrect);
            validChange = false;
        }

        if (validChange) {
            dummyPwd = getNewPassword();
            try {
                DBConnection dbcon = new DBConnection();
                dbcon.establishConnection();
                Statement stmt = dbcon.con.createStatement();
                stmt.executeUpdate("UPDATE account SET password = \""+dummyPwd+"\" where account.Account_ID = "+ClientFrame.getAccountId());
                dbcon.con.close();
            } catch (Exception ex) {
                System.out.println(ex.toString());
            }
            resetNewPwdForm();
            ChangePasswordDialog.dispose();
        }
    }//GEN-LAST:event_ConfirmBtnActionPerformed

