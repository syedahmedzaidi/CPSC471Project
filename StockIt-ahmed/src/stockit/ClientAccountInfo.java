    private void checkNewPassword() {
        if (getConfirmedPassword().length() > 0) {
            if (getNewPassword().equals(getConfirmedPassword())) {
                NewPasswordCheck.setVisible(true);
                NewPasswordX.setVisible(false);
            } else {
                NewPasswordCheck.setVisible(false);
                NewPasswordX.setVisible(true);
            }
        }
    }

    private void hideConfirmationLabels() {
        CurrentPasswordIncorrect.setVisible(false);
        NewPasswordCheck.setVisible(false);
        ConfirmNewPasswordIncorrect.setVisible(false);
        NewPasswordIncorrect.setVisible(false);
        NewPasswordX.setVisible(false);
    }

    private void invalidateField(JPasswordField pwdField, JLabel label) {
        label.setVisible(true);
        pwdField.setBackground(new Color(255, 51, 51, 150));
    }

    private void validateField(JPasswordField pwdField, JLabel label) {
        label.setVisible(false);
        pwdField.setBackground(new Color(255, 255, 255, 255));
    }

    private void resetNewPwdForm() {
        validateField(ConfirmNewPasswordField, ConfirmNewPasswordIncorrect);
        validateField(NewPasswordField, NewPasswordIncorrect);
        validateField(CurrentPasswordField, CurrentPasswordIncorrect);
        NewPasswordCheck.setVisible(false);
        NewPasswordX.setVisible(false);
        ConfirmNewPasswordField.setText("");
        NewPasswordField.setText("");
        CurrentPasswordField.setText("");
    }

    private void fetchPassword() {
        try {
            DBConnection dbcon = new DBConnection();
            dbcon.establishConnection();
            Statement stmt = dbcon.con.createStatement();
            String container = ClientFrame.getAccountId();
            ResultSet rs = stmt.executeQuery("select password from account where account.Account_ID = " + ClientFrame.getAccountId());
            while (rs.next()) {
                dummyPwd = rs.getString("password");
                System.out.println(dummyPwd);
            }
            dbcon.con.close();
        } catch (Exception ex) {
            System.out.println(ex.toString());
        }
    }

